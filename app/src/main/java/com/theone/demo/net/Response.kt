class Response<T>(
    var error_code: Int,
    var reason: String?,
    var result: T?,
    var pageInfo: PageInfo?
) : IResponse<T> {

    override fun isSuccess(): Boolean = error_code == 0

    override fun getData(): T? = result

    override fun getPageInfo(): IPageInfo? = pageInfo

    override fun getMsg(): String? = reason
}